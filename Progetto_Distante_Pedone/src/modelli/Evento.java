package modelli;

public class Evento 
{
	public String Nome;
	public String Stato;
	
	public void setNome(String nome)
	{
		
		this.Nome  = nome;
		
	}
	
	public void setStato(String stato)
	{
		
		this.Stato = stato;
		
	}
	
	public String getNome() 
	{
		
		
		return Nome;
	}

	public String getStato() 
	{
		
		return Stato;
	}

}
